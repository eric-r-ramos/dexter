package io.dexter.services.dispatcher.entities;

/**
 * GeoLocation entity from service provider
 * 
 * @author ericramos
 *
 */
public class GeoLocation {

	private float lat;
	private float lng;
	
	public float getLat() {
		return lat;
	}
	public void setLat(float lat) {
		this.lat = lat;
	}
	public float getLng() {
		return lng;
	}
	public void setLng(float lng) {
		this.lng = lng;
	}
	
}
